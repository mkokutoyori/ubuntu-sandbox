describe('debug — security: ACL / firewall / port-security across the enterprise WAN', () => {
  it('exercises Cisco ACLs, Huawei traffic-filter, Linux iptables/ufw and port-security', async () => {
    resetSim();
    const wan = buildEnterpriseWAN();
    await initializeWAN(wan);
    await addInternetEdge(wan);

    const steps = buildSteps();
    expect(steps.length).toBeGreaterThanOrEqual(400);

    await dumpProtocol(
      'acl-security',
      wan.topology,
      steps,
      'security=ACL/pare-feu/port-security — ACL Cisco (standard/étendue/nommée, edge firewall, ' +
      'access-class VTY), ACL Huawei (basic 2000/advanced 3000 + traffic-filter), pare-feu Linux ' +
      '(iptables/ufw/nft/conntrack), port-security L2 Cisco/Huawei — bordure Internet simulée',
    );
  }, 300000);
});
