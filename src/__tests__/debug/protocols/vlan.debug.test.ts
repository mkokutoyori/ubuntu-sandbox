describe('debug — protocol VLAN / 802.1Q trunking & inter-VLAN routing', () => {
  it('exercises VLAN database, trunks, access ports, router-on-a-stick, VTP/DTP across vendors', async () => {
    resetSim();
    const wan = buildEnterpriseWAN();
    addStpFabric(wan);
    await initializeWAN(wan);

    const steps = buildSteps();
    expect(steps.length).toBeGreaterThanOrEqual(400);

    await dumpProtocol(
      'vlan',
      wan.topology,
      steps,
      'protocol=VLAN / IEEE 802.1Q — base VLAN, trunks dot1q (allowed/native/pruning), ports ' +
      'd\'accès, voice VLAN, VTP/DTP, VLANIF/SVI, MAC & STP par VLAN, et inter-VLAN routé ' +
      'fonctionnel (router-on-a-stick R-HQ) — Cisco (SW-HQ/DSW) + Huawei (SW-BR/DSW-BR) + generic',
    );
  }, 300000);
});
