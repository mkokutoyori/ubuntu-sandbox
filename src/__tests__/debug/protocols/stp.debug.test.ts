describe('debug — protocol STP/RSTP/MSTP (IEEE 802.1D/w/s) across the enterprise WAN', () => {
  it('exercises root election, port roles, loop breaking, guards and convergence across vendors', async () => {
    resetSim();
    const wan = buildEnterpriseWAN();
    addStpFabric(wan);
    await initializeWAN(wan);

    const steps = buildSteps();
    expect(steps.length).toBeGreaterThanOrEqual(300);

    await dumpProtocol(
      'stp',
      wan.topology,
      steps,
      'protocol=STP/RSTP/MSTP (IEEE 802.1D/802.1w/802.1s) — focus: élection racine, rôles/états ' +
      'de ports, blocage de boucle, coût/priorité, PortFast/edged, BPDU/Root/Loop guard, timers, ' +
      'convergence sur panne, régions MST — fabric redondante Cisco (triangle HQ) + Huawei (BR)',
    );
  }, 300000);
});
