describe('debug — Linux processes & services (PC + server)', () => {
  it('exercises processes & services from the bash terminal', async () => {
    const commands = buildCommands();
    expect(commands.length).toBeGreaterThanOrEqual(300);

    const pc = new LinuxPC('linux-pc', 'L-PC-SP-DBG', 120, 120);
    await runAndDump(
      'linux-pc-services-processes',
      commands,
      createLinuxRunner(pc),
      'host=L-PC-SP-DBG (linux-pc) — focus: processus & services',
      'linux',
    );

    const srv = new LinuxServer('linux-server', 'L-SRV-SP-DBG', 200, 200);
    await runAndDump(
      'linux-server-services-processes',
      commands,
      createLinuxRunner(srv),
      'host=L-SRV-SP-DBG (linux-server) — focus: processus & services',
      'linux',
    );
  }, 240_000);
});
