/**
 * Debug run — Linux PROCESSES & SERVICES on a PC and a server.
 *
 * Replays a large, structured command list through the in-browser bash
 * terminal of a `LinuxPC` and a `LinuxServer`. The intent is purely
 * diagnostic: no assumptions are made about which commands are
 * implemented — the transcript records success, error message, or JS
 * exception verbatim so a human can diff it and decide what to fix.
 *
 * Coverage strategy:
 *   • inventory commands (ps / pstree / pgrep / top / /proc)
 *   • the SET-then-SHOW pattern: mutate state with one command, read it
 *     back with another (nice/renice, ulimit, sysctl, systemctl
 *     enable/disable, service start/stop, crontab)
 *   • composed scenarios (set → verify → restore)
 *   • deliberately MALFORMED commands and error-path inputs
 *
 * Transcripts →
 *   debug-output/linux/linux-pc-services-processes_results_debug.txt
 *   debug-output/linux/linux-server-services-processes_results_debug.txt
 */

